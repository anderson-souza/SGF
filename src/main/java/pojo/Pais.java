package pojo;

/**
 * @author Anderson
 */
public class Pais {

    private int id;
    private String nome;
    private String obs;
    private String sitpai;

    public String getSitpai() {
        return sitpai;
    }

    public void setSitpai(String sitpai) {
        this.sitpai = sitpai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getObs() {
        return obs;
    }

    public void setObs(String obs) {
        this.obs = obs;
    }

}
